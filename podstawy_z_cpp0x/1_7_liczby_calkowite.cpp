#include <iostream>

int main(){
    int a, b;
    std::cout << "Podaj a: ";
    std::cin >> a;
    std::cout << std::endl;
    std::cout << "Podaj b: ";
    std::cin >> b;
    std::cout << std::endl;
    std::cout << "a + b = " << a + b << std::endl;
    std::cout << "a - b = " << a - b << std::endl;
    std::cout << "a * b = " << a * b << std::endl;
    std::cout << "a / b = " << a / b << std::endl;
    std::cout << "a % b = " << a % b << std::endl;
}